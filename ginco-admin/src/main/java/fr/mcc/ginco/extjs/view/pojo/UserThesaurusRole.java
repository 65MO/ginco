package fr.mcc.ginco.extjs.view.pojo;

public class UserThesaurusRole {
	
	private String thesaurusId;
	private Integer role;
	
	public String getThesaurusId() {
		return thesaurusId;
	}
	public void setThesaurusId(String thesaurusId) {
		this.thesaurusId = thesaurusId;
	}
	public Integer getRole() {
		return role;
	}
	public void setRole(Integer role) {
		this.role = role;
	}
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((role == null) ? 0 : role.hashCode());
		result = prime * result
				+ ((thesaurusId == null) ? 0 : thesaurusId.hashCode());
		return result;
	}
	@Override
	public boolean equals(Object obj) {
		if (this == obj)  {
			return true;
		}
		if (obj == null) {
			return false;
		}
		if (getClass() != obj.getClass()) {
			return false;
		}
		UserThesaurusRole other = (UserThesaurusRole) obj;
		if (role == null) {
			if (other.role != null) {
				return false;
			}
		} else if (!role.equals(other.role)) {
			return false;
		}
		if (thesaurusId == null) {
			if (other.thesaurusId != null) {
				return false;
			}
		} else if (!thesaurusId.equals(other.thesaurusId)) {
			return false;
		}
		return true;
	}	

}
